# HUMAN HIGHER FUNCTIONS HAVE EVOLVED FOR FITNESS OF CERTAIN TASKS / CONTEXTS.

"""PERCEPTION"""
# Human perception has evolved to be particularly good at detecting patterns
# and make quick judgements about potential threats or opportunities.

"""ATTENTION"""
# Attentional processes have evolved to be flexible and adaptive, allowing us
# to switch between different tasks and adjust our focus as needed.

"""MEMORY"""
# Human memory has evolved to be highly selective and efficient, allowing us to
# retain information that is relevant to our goals and discard information that
# is not.

"""LANGUAGE"""
# Human language has evolved to be highly flexible and adaptive, allowing us to
# convey meaning in a wide variety of contexts.

"""REASONING AND PROBLEM SOLVING"""
# These higher functions allow us to use past experiences and knowledge to
# generate creative solutions to new problems.

"""EMOTION AND MOTIVATION"""
# Emotions help us evaluate the significance of events and guide our responses,
# while motivation drives us to pursue goals and take action to achieve them.
